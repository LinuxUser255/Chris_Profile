            <h1 style="font-weight: bold; font-size: 40px; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">Chris Bingham: My Path to Cybersecurity</h1>
            <br>
            <p style="font-weight: bold; font-style: italic; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">These bios are always hard—where do I even start?</p>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">My journey into Cybersecurity was anything but linear.</p>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">In university, I majored in Arabic and Persian.<br>

                I've always had a passion for foreign languages, and becoming multilingual was a personal goal I'm proud to have achieved.<br>
                Unfortunately, career opportunities in that field were limited—at least none that truly appealed to me.</p>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">
            <br style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">
                I had an interest in law enforcement but wasn't sure I wanted to commit without first exploring something adjacent.<br>
                So, I became a security guard. I worked both unarmed and armed roles. It was adventurous at times—some parts I liked, others not so much.<br>
                But I have to say: it's not a sustainable career financially, and as a result, decided not to got the Police route.<br>
                So I soon found myself back at square one.</p>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">
                <br>
                Next, I explored machining.<br>
                I thought it might be fun, but it quickly became clear it wasn't for me.<br>
                I ended up running in a metaphorical hamster wheel on a CNC machining assembly line, later operating plasma cutters<br>
                and grinding steel parts six days a week under mandatory overtime.<br>
                If you didn't show up, you were fired.</p>
            <br>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">Every day, I came home covered in steel dust.<br>
                If you've never worked in that environment, imagine the engine room scene from Titanic—that's what it felt like.<br>
                Long story short: manufacturing wasn't for me. Those were some toxic work environments, and I didn't have the temperament for it.<br>
                I have deep respect for those who thrive in that field—I had great coworkers—but the grind wore me down.</p>
            <p style="font-weight: bold; font-style: italic; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">I spent a few years lost, knowing I was capable of more, but unsure how to break free.<br>
                Those were demoralizing years.<br>
                Then one day, I became curious about hacking.<br>
                I asked myself, "How does that work?" That question changed everything.</p>
            <br>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">I dove headfirst into the world of Cybersecurity with a level of tenacity and hunger that can only be described as obsession.<br>
                My two goals were clear: escape manufacturing and transition into Cybersecurity.</p>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">I had no prior tech experience, but the same intellectual curiosity that drove me to learn foreign languages pushed me into tech.<br>
                After a couple of years of self-study and hustle, I landed my first role as a SOC Analyst.</p>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">That job only lasted two and a half months.<br>
                <br>
                I started in January 2020, and when the pandemic hit in March, my company implemented a LIFO (Last In, First Out) policy.<br>
                I was among the first to be laid off.</p>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">Since then, I've worn many hats in Cybersecurity: SOC Analyst, co-founder of BSides Triad, online Linux and Python course creator, web and network penetration tester, CI/CD pipeline security specialist, Windows enterprise network pen tester, and more.</p>
            <p style="font-weight: bold; text-align: left; text-align: left; font-family: 'Special Elite', cursive; margin-left: 20px;">Today, I work as a Web App Security Consultant and contributor to HARDN, an open-source project focused on hardening GNU/Linux systems—specifically Debian.</p>
